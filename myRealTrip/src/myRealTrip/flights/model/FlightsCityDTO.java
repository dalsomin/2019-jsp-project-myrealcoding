package myRealTrip.flights.model;

public class FlightsCityDTO {
	private String c_name;
	private String ap_code;
	private String ap_city;
	private String n_name;
	
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getAp_code() {
		return ap_code;
	}
	public void setAp_code(String ap_code) {
		this.ap_code = ap_code;
	}
	public String getN_name() {
		return n_name;
	}
	public void setN_name(String n_name) {
		this.n_name = n_name;
	}
	public String getAp_city() {
		return ap_city;
	}
	public void setAp_city(String ap_city) {
		this.ap_city = ap_city;
	}
	
}
