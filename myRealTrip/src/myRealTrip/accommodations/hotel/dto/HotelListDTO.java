package myRealTrip.accommodations.hotel.dto;

public class HotelListDTO {
	
	private String h_code;
	private String h_pic;
	private String h_name;
	private String h_grade;
	private String h_price;
	private String h_b_sname;
	private int cnt;
	private double ravg;
	private String grade;
	
	
	public String getH_code() {
		return h_code;
	}
	public void setH_code(String h_code) {
		this.h_code = h_code;
	}
	public String getH_pic() {
		return h_pic;
	}
	public void setH_pic(String h_pic) {
		this.h_pic = h_pic;
	}
	public String getH_name() {
		return h_name;
	}
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	public String getH_grade() {
		return h_grade;
	}
	public void setH_grade(String h_grade) {
		this.h_grade = h_grade;
	}
	public String getH_price() {
		return h_price;
	}
	public void setH_price(String h_price) {
		this.h_price = h_price;
	}
	public String getH_b_sname() {
		return h_b_sname;
	}
	public void setH_b_sname(String h_b_sname) {
		this.h_b_sname = h_b_sname;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public double getRavg() {
		return ravg;
	}
	public void setRavg(double ravg) {
		this.ravg = ravg;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
}
